//Transpose the array

public class Transpose{
  private static void print(int [][] arr){
     for(int i=0; i<=arr.length; i++){
        for(int ele:arr[i]){
          System.out.println(ele);
        }
     }
  }
  public static void main(String[] args) {
    int[][] arr = { { 6, 0, 2, 7, 9 }, { 1, 3, 7, 2, 5 }, { 9, 1, 4, 5, 2 } };

  }                                                
}